-- ============================================================
-- VER SKUS CON MÁS PUBLICACIONES
-- ============================================================
SELECT 
    sku, 
    COUNT(*) as cant_publicaciones,
    GROUP_CONCAT(mla_id ORDER BY mla_id SEPARATOR ', ') as publicaciones
FROM sku_mla_mapeo 
GROUP BY sku 
HAVING COUNT(*) > 5
ORDER BY cant_publicaciones DESC;
